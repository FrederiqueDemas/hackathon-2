const models = require("../models");

class ProjectsLanguagesController {
  static browse = (req, res) => {
    models.projects_languages
      .findAll()
      .then(([rows]) => {
        res.send(rows);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static read = (req, res) => {
    models.projects_languages
      .findByProjectId(req.params.id)
      .then(([rows]) => {
        if (rows[0] == null) {
          res.sendStatus(404);
        } else {
          res.send(rows);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static add = (req, res) => {
    const projectsLanguages = req.body;

    models.projects_languages
      .insert(projectsLanguages)
      .then(() => {
        res.status(201).send({ ...projectsLanguages });
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static delete = (req, res) => {
    models.projects_languages
      .delete(req.params.language_id, req.params.project_id)
      .then(() => {
        res.sendStatus(204);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };
}

module.exports = ProjectsLanguagesController;
