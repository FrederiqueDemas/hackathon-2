const models = require("../models");

class ProjectsKeywordsController {
  static browse = (req, res) => {
    models.projects_keywords
      .findAll()
      .then(([rows]) => {
        res.send(rows);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static read = (req, res) => {
    models.projects_keywords
      .findByProjectId(req.params.id)
      .then(([rows]) => {
        if (rows[0] == null) {
          res.sendStatus(404);
        } else {
          res.send(rows[0]);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static add = (req, res) => {
    const projectsKeywords = req.body;

    models.projects_keywords
      .insert(projectsKeywords)
      .then(() => {
        res.status(201).send({ ...projectsKeywords });
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static delete = (req, res) => {
    models.projects_keywords
      .delete(req.params.keyword_id, req.params.project_id)
      .then(() => {
        res.sendStatus(204);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };
}

module.exports = ProjectsKeywordsController;
